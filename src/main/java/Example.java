public class Example {

    String name;
    int number;
    boolean isCool;

    public Example(String name, int number, boolean isCool) {
        this.name = name;
        this.number = number;
        this.isCool = isCool;
    }

    public String getName() {
        return this.name;
    }

    public int getNumber() {
        return this.number;
    }

    public boolean isCool() {
        return this.isCool;
    }
}
